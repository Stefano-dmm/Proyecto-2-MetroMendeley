    private final Path pathArchivo = Paths.get("AlmacenHash", "tablaHash.txt");
    private void actualizarListaInvestigaciones() {
        // Crear un modelo de lista para gestionar los elementos de jList1
        DefaultListModel<String> modelo = new DefaultListModel<>();
        // Leer los nombres de las investigaciones desde el archivo tablaHash.txt
        try (BufferedReader reader = Files.newBufferedReader(pathArchivo)) {
            String linea;
            while ((linea = reader.readLine()) != null) {
                // Dividir la línea basándose en "=" y tomar la parte antes de "="
                String[] partes = linea.split("=");
                if (partes.length > 0) { // Verificar si existe "=" en la línea
                    // Tomar todo lo que está antes de "=" como el título
                    String titulo = partes[0].trim();
                    modelo.addElement(titulo);
                }
            }
            // Asignar el modelo a jList1
            jList1.setModel(modelo);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    
