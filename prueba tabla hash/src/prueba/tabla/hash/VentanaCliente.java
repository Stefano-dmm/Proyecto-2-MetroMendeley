   private void eliminarInvestigacionSeleccionada() {
        // Obtener el título seleccionado de la lista
        String tituloSeleccionado = jList1.getSelectedValue();
        if (tituloSeleccionado != null) {
            // Llamar a la función eliminarInvestigacion con el título seleccionado
            TablaHash.eliminarInvestigacion(tituloSeleccionado);
        } else {
            // Mostrar un mensaje si no se ha seleccionado nada
            JOptionPane.showMessageDialog(null, "Por favor, seleccione una investigación de la lista para eliminar.");
        }
    }
    
